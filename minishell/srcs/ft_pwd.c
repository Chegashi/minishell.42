#include "../minishell.h"

void	ft_pwd(t_pipes *pipe)
{
	char	cwd[1024];

	if (getcwd(cwd, sizeof(cwd)) != NULL)
	{
		ft_putstr(cwd);
		ft_putstr("\n");
	}
}
